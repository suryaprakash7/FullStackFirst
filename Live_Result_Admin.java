    private void EnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterActionPerformed
       con=Javav1.getConnection();
       
       if(con !=null){
           
           String id=jID.getText();
           String semester=(String) jSemester.getSelectedItem();
           String result=jResult.getText();
           
           String sql = "INSERT INTO Result(ID,Semester,Result) VALUES (?,?,?)";
           
           try {
               pst=con.prepareStatement(sql);
               pst.setInt(1, Integer.parseInt(id));
               pst.setString(2,semester);
               pst.setString(3,result);
               pst.execute();
               
               JOptionPane.showMessageDialog(null,"Succesfully Added");
           } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null,"Invalid Entry");
           }
           finally{
           try{
           rs.close();
           pst.close();
           }
           catch(Exception ex){}
           }
       }
       ResultDB();
    }//GEN-LAST:event_EnterActionPerformed

    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        int row=jTable.getSelectedRow();
        String sjb=jTable.getModel().getValueAt(row, 0).toString();
        
        try{
        String sqwl="select * from Result where Ass_ID="+sjb+"";
        pst=con.prepareStatement(sqwl);
        rs=pst.executeQuery();
        
        if(rs.next()){
          
           int id=rs.getInt("ID");
           String semester=rs.getString("Semester");
           String result=rs.getString("Result");
          
           
           
           jID.setText(""+id);
           jSemester.setSelectedItem(semester);
           jResult.setText(result);
        }
        
        }
        catch(Exception ex){
        JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jTableMouseClicked

